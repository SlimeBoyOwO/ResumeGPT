async def seed_default_experts() -> None:
    """Insert new experts and update existing ones to keep prompts current."""
    async with async_session_factory() as session:
        result = await session.execute(select(Expert))
        existing = {e.name: e for e in result.scalars().all()}

        changed = False
        for expert_data in DEFAULT_EXPERTS:
            name = expert_data["name"]
            if name in existing:
                record = existing[name]
                if (
                    record.description != expert_data["description"]
                    or record.system_prompt != expert_data["system_prompt"]
                ):
                    record.description = expert_data["description"]
                    record.system_prompt = expert_data["system_prompt"]
                    changed = True
            else:
                session.add(Expert(**expert_data))
                changed = True

        if changed:
            await session.commit()
